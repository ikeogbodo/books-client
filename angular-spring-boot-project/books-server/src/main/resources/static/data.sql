create table tele 
(id integer not null auto_increment, 
	first_name varchar(150), 
	last_name varchar(150), 
	address varchar(255), 
	city varchar(150), 
	state varchar(150), 
	zip varchar(150), 
	homephone varchar(150), 
	workphone varchar(150), 
	cellphone varchar(150), 	
	email varchar(255), 	
	primary key (id)
);